package chapter.six.one.one;

public class Assign
        //类定义
{
    public static void main(String[] args) {  //主方法定义
        byte b = 50;       //字节类型赋值
        float f = 0.55F;   //浮点数类型赋值
        long l = 0xfffL;   //64 位整型赋值
        System.out.println("b = " + b); //打印结果
        System.out.println("f = " + f);
        System.out.println("l = " + l);
    }
}



